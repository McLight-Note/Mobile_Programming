fun main(){
    val name: String = "Muhammad"
    val age: Int = 24
    println("$name is $age years old")

    val number = 23
    val integerMaxValue = Int.MAX_VALUE
    val integerMinValue = Int.MIN_VALUE
    println("Integer Max Value is $integerMaxValue")
    println("Integer Minimum Value is $integerMinValue")

    val byteMaxValue = Byte.MAX_VALUE
    val byteMinValue = Byte.MIN_VALUE
    println("Byte Max Value is $byteMaxValue")
    println("Byte Minimum Value is $byteMinValue")

    val shortMaxValue = Short.MAX_VALUE
    val shortMinValue = Short.MIN_VALUE
    println("Short Max Value is $shortMaxValue")
    println("Short Minimum Value is $shortMinValue")

    val longMaxValue = Long.MAX_VALUE
    val longMinValue = Long.MIN_VALUE
    println("Long Max Value is $longMaxValue")
    println("Long Minimum Value is $longMinValue")

    // Float numbers

    val myNumber: Float = 3.13f
    val mySecondNumber = 3.0

    println("myNumber is $myNumber and mySecondNumber is $mySecondNumber")

    // Boolean and Char

    val myCharValue: Char = 'D'
    println("myCharValue is $myCharValue")

    val myBoolean: Boolean = true
    val myTrue: Boolean = true
    val myFalse: Boolean = false
    println("myBoolean is $myBoolean")
    println("myTrue is $myTrue")
    println("myFalse is $myFalse")
}