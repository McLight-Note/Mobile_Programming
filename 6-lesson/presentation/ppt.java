public class FloatVsDouble {
    public static void main(String[] args) {
        // Float
        float a = 56.2F, b = 12.8F;
        System.out.println("Float addition:    " + (a + b));

        float g = 12.4F, u = 1.8F;
        System.out.println("Float subtraction: " + (g - u));

        // Double
        double l = 2.2, k = 1.9;
        System.out.println("Double multiply:   " + (l * k));

        double ww = 6.2, dd = 2.1;
        System.out.println("Double division:   " + (ww / dd));
    }
}

